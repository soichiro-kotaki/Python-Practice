#  printメソッドで出力
print("Good morning")
print("kotaki")

#  変数宣言　Javascriptと違い、const、letなどの記述は不要
numberList = [ 1, 2, 3, 4, 5, 6]
#  リストを出力
print(numberList)

#  データ型
#  数値型　小数点は「float型」整数は「int」型
num01 = 123
num02 = 1.23
#  typeメソッドでデータ型を確認
print(type(num01))
print(type(num02))

#  文字列型(string型）
str01 = "kotaki"
print(type(str01))

#  bool型（真偽型）
bool01 = 10 < 1
print(bool01)
print(type(bool01))

#  おまけ
x = 3.13232453553
print(round(x, 4))



